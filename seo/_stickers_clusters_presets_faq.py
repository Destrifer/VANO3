# ── применение ─────────────────────────────────────────────────────────────
pages = req('GET', '/items/promoted_pages?filter[product][_eq]=%d&limit=-1'
                   '&fields=id,slug,preset,faq.faq_items_id.id,faq.faq_items_id.question' % PID)['data']
by_slug = {p['slug']: p for p in pages}

print('=== ПРЕСЕТЫ ===')
seen = {}
changed_p = 0
for slug, want in PRESETS.items():
    page = by_slug.get(slug)
    if not page: print('!! нет кластера', slug); continue
    cur = page['preset'] or None
    if want is not None:
        key = json.dumps(want, sort_keys=True, ensure_ascii=False)
        if key in seen: print('!! ДУБЛЬ пресета: %s == %s (%s)' % (slug, seen[key], key))
        seen[key] = slug
    if (cur or None) == want:
        print('=  %-14s %s' % (slug, json.dumps(want, ensure_ascii=False))); continue
    changed_p += 1
    print('%s %-14s %s -> %s' % ('→' if APPLY else '~', slug,
          json.dumps(cur, ensure_ascii=False), json.dumps(want, ensure_ascii=False)))
    if APPLY:
        req('PATCH', '/items/promoted_pages/%d' % page['id'], {'preset': want if want else {}})

print('\n=== FAQ ===')
existing_q = {}
for it in req('GET', '/items/faq_items?limit=-1&fields=id,question')['data']:
    existing_q[it['question'].strip()] = it['id']

added = 0
for slug, items in FAQ.items():
    page = by_slug.get(slug)
    if not page: print('!! нет кластера', slug); continue
    have = {f['faq_items_id']['question'].strip() for f in (page['faq'] or [])}
    for q, a in items:
        if q.strip() in have:
            print('=  %-14s %s' % (slug, q)); continue
        added += 1
        print('%s %-14s %s' % ('→' if APPLY else '~', slug, q))
        if APPLY:
            fid = existing_q.get(q.strip())
            if not fid:
                fid = req('POST', '/items/faq_items', {'question': q, 'answer': a})['data']['id']
                existing_q[q.strip()] = fid
            req('POST', '/items/promoted_pages_faq_items',
                {'promoted_pages_id': page['id'], 'faq_items_id': fid})

print('\n=== ПРАВКА НЕВЕРНЫХ ОТВЕТОВ ===')
fixed = 0
for fid, (q, a) in FIX_ANSWERS.items():
    cur = req('GET', '/items/faq_items/%d?fields=question,answer' % fid)['data']
    if cur['answer'].strip() == a.strip():
        print('=  %d %s' % (fid, q)); continue
    fixed += 1
    print('%s %d %s' % ('→' if APPLY else '~', fid, q))
    print('   было:  %s' % cur['answer'][:90])
    print('   стало: %s' % a[:90])
    if APPLY:
        req('PATCH', '/items/faq_items/%d' % fid, {'answer': a})

print('\n%s: пресетов %d, вопросов %d, правок ответов %d'
      % ('Записано' if APPLY else 'К записи (dry-run)', changed_p, added, fixed))
if not APPLY:
    print('Применить: python seo/_stickers_clusters_presets_faq.py --apply')
